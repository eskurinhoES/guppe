"""
Programa que lê três números do usuário e mostra a soma total
entre eles.
"""
number1 = int(input('Digite o primeiro valor: '))
number2 = int(input('Digite o segundo valor: '))
number3 = int(input('Digite o terceiro valor: '))
soma_total = number1 + number2 + number3

print(f'Você digitou os seguintes valores: {number1}, {number2} e {number3}')
print(f'E a soma total entre eles foi: {soma_total}')
